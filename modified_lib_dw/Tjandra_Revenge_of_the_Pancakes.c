#include<stdio.h>
typedef unsigned u;
int main()
{
  int first_iteration;
  u t = 0;
  u T;
  u i;
  u j;
  u k;
  char S[111];
  for (scanf("%u", &T); (t++) < T; printf("Case #%u: %u\n", t, k))
  {
    scanf("%s", S);
    for (i = -1; S[++i];)
      ;

    S[i] = '+';
    for (j = (k = 0); (++j) <= i;)
      if (S[j] != S[j - 1])
      ++k;


  }

  return 0;
}


